$(document).keyup((e) => {
    if (e.key === "Escape") {
        hideImage();
    }
});

let isShowCaseVisible = false;
function showImage(imageURL) {
    const showcase = document.getElementById("js-gallery-showcase");
    const image = document.getElementById("js-showcase-image");
    image.src = imageURL;
    isShowCaseVisible = !isShowCaseVisible;
    isShowCaseVisible ?
        showcase.classList.toggle("show", isShowCaseVisible):
        showcase.classList.toggle("show", isShowCaseVisible);
}

function hideImage() {
    const showcase = document.getElementById("js-gallery-showcase");
    isShowCaseVisible = false;
    showcase.classList.toggle("show", false);
}

function loadImages() {
    let rowElement = "";
    gallery.forEach((image, key) => {
        rowElement += loadTemplate("js-row-template", image, key);
    });
    document.getElementById("js-gallery-body").innerHTML = rowElement;
}

function loadTemplate(templateId, image) {
    let template = document.getElementById(templateId).innerHTML;
    return eval("`" + template + "`");
}